package yoo.calender;

public class TodoListVO {

	private int tdl_no;
	private String tdl_date;
	private String tdl_contents;
	private String tdl_category;
	private int member_no;
	
	
	public int getTdl_no() {
		return tdl_no;
	}
	public void setTdl_no(int tdl_no) {
		this.tdl_no = tdl_no;
	}
	public String getTdl_date() {
		return tdl_date;
	}
	public void setTdl_date(String tdl_date) {
		this.tdl_date = tdl_date;
	}
	public String getTdl_contents() {
		return tdl_contents;
	}
	public void setTdl_contents(String tdl_contents) {
		this.tdl_contents = tdl_contents;
	}
	public String getTdl_category() {
		return tdl_category;
	}
	public void setTdl_category(String tdl_category) {
		this.tdl_category = tdl_category;
	}
	public int getMember_no() {
		return member_no;
	}
	public void setMember_no(int member_no) {
		this.member_no = member_no;
	}
	
	
}	
